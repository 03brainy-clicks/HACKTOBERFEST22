public class half_pyrmaid_pattern {
    public static void main(String args[]) {
          for(int i =1; i<=5; i++){
            //for numbers printing
            for(int j =1; j<=i; j++){
                System.out.print(j);
            }
            // for spaces printing
            for(int j=1; j<=(5-i); j++){
                System.out.print(" ");
            }

            System.out.println();








        }
} }